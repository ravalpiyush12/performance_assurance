-- =============================================================================
-- 2. API_NFT_USER_LOB_ACCESS
--    Maps API_AUTH_USERS to the LOBs they can access
--    Used by PE/test_lead login to show only their assigned LOBs
-- =============================================================================
CREATE TABLE API_NFT_USER_LOB_ACCESS (
    ACCESS_ID       NUMBER          NOT NULL,
    USERNAME        VARCHAR2(100)   NOT NULL,
    LOB_NAME        VARCHAR2(100)   NOT NULL,
    GRANTED_BY      VARCHAR2(100)   NOT NULL,
    GRANTED_DATE    DATE            DEFAULT SYSDATE NOT NULL,
    IS_ACTIVE       CHAR(1)         DEFAULT 'Y' NOT NULL,
    REVOKED_BY      VARCHAR2(100),
    REVOKED_DATE    DATE,
    CONSTRAINT PK_NFT_USER_LOB      PRIMARY KEY (ACCESS_ID),
    CONSTRAINT UK_NFT_USER_LOB      UNIQUE (USERNAME, LOB_NAME),
    CONSTRAINT CHK_NFT_ULA_ACTIVE   CHECK (IS_ACTIVE IN ('Y','N')),
    CONSTRAINT FK_NFT_ULA_USER      FOREIGN KEY (USERNAME)
        REFERENCES API_AUTH_USERS(USERNAME) ON DELETE CASCADE
);
COMMENT ON TABLE  API_NFT_USER_LOB_ACCESS IS
    'Maps users to accessible LOBs - admin grants access for PE/test_lead roles';
COMMENT ON COLUMN API_NFT_USER_LOB_ACCESS.IS_ACTIVE IS 'Y=active, N=revoked';
CREATE INDEX IDX_NFT_ULA_USERNAME ON API_NFT_USER_LOB_ACCESS(USERNAME);
CREATE INDEX IDX_NFT_ULA_LOB      ON API_NFT_USER_LOB_ACCESS(LOB_NAME);
CREATE INDEX IDX_NFT_ULA_ACTIVE   ON API_NFT_USER_LOB_ACCESS(IS_ACTIVE);

