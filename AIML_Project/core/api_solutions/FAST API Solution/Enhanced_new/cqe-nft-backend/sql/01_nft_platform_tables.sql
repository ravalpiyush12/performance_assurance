-- 10. API_NFT_RELEASE_REPORTS
--     Final test report HTML stored as CLOB - 12+ month retention
-- =============================================================================
CREATE TABLE API_NFT_RELEASE_REPORTS (
    REPORT_ID       NUMBER          NOT NULL,
    RUN_ID          VARCHAR2(100)   NOT NULL,
    LOB_NAME        VARCHAR2(100)   NOT NULL,
    TRACK           VARCHAR2(50),
    TEST_NAME       VARCHAR2(200),
    TEST_TYPE       VARCHAR2(50),
    PC_RUN_ID       VARCHAR2(50),
    REPORT_TITLE    VARCHAR2(500),
    REPORT_HTML     CLOB,
    REPORT_SIZE_KB  NUMBER,
    GENERATED_BY    VARCHAR2(100),
    GENERATED_DATE  DATE            DEFAULT SYSDATE NOT NULL,
    RETAIN_UNTIL    DATE,
    IS_ACTIVE       CHAR(1)         DEFAULT 'Y' NOT NULL,
    NOTES           VARCHAR2(2000),
    CONSTRAINT PK_NFT_REPORT       PRIMARY KEY (REPORT_ID),
    CONSTRAINT UK_NFT_REPORT_RUN   UNIQUE (RUN_ID),
    CONSTRAINT CHK_NFT_RPT_ACTIVE  CHECK (IS_ACTIVE IN ('Y','N')),
    CONSTRAINT CHK_NFT_RPT_TYPE    CHECK (
        TEST_TYPE IN ('LOAD','STRESS','ENDURANCE','SPIKE','SOAK')
        OR TEST_TYPE IS NULL),
    CONSTRAINT FK_NFT_RPT_RUN      FOREIGN KEY (RUN_ID)
        REFERENCES API_RUN_MASTER(RUN_ID)
);
COMMENT ON TABLE  API_NFT_RELEASE_REPORTS IS
    'Final test report HTML CLOB - 12+ month retention policy';
COMMENT ON COLUMN API_NFT_RELEASE_REPORTS.REPORT_HTML  IS
    'Complete HTML report including all tool data and AI RCA';
COMMENT ON COLUMN API_NFT_RELEASE_REPORTS.RETAIN_UNTIL IS
    'NULL=keep forever, date=auto-cleanup eligible after this date';
CREATE INDEX IDX_NFT_RPT_LOB    ON API_NFT_RELEASE_REPORTS(LOB_NAME);
CREATE INDEX IDX_NFT_RPT_RUN    ON API_NFT_RELEASE_REPORTS(RUN_ID);
CREATE INDEX IDX_NFT_RPT_DATE   ON API_NFT_RELEASE_REPORTS(GENERATED_DATE);
CREATE INDEX IDX_NFT_RPT_ACTIVE ON API_NFT_RELEASE_REPORTS(IS_ACTIVE);

