-- ============================================================
-- 8. TRACK TEMPLATE
--    Maps all tool configs to a LOB/Track.
--    This is the master template PE loads at monitoring start.
--    IDs are comma-separated FKs to respective config tables.
-- ============================================================
CREATE TABLE API_NFT_TRACK_TEMPLATE (
    TEMPLATE_ID        NUMBER         DEFAULT API_NFT_TRACK_SEQ.NEXTVAL PRIMARY KEY,
    LOB_NAME           VARCHAR2(100)  NOT NULL,
    TRACK_NAME         VARCHAR2(100)  NOT NULL,
    -- Comma-separated IDs from config tables
    APPD_APP_IDS       VARCHAR2(4000),  -- APP_MASTER_IDs from APPD_APPLICATIONS_MASTER
    KIBANA_CONFIG_IDS  VARCHAR2(1000),  -- KIBANA_CONFIG_IDs from API_NFT_KIBANA_CONFIG
    SPLUNK_CONFIG_IDS  VARCHAR2(1000),  -- SPLUNK_CONFIG_IDs from API_NFT_SPLUNK_CONFIG
    MONGODB_CONFIG_IDS VARCHAR2(1000),  -- MONGODB_CONFIG_IDs from API_NFT_MONGODB_CONFIG
    PC_CONFIG_IDS      VARCHAR2(1000),  -- PC_CONFIG_IDs from API_NFT_PC_CONFIG
    DB_CONFIG_IDS      VARCHAR2(1000),  -- DB_CONFIG_IDs from API_NFT_DB_CONFIG
    -- Summary counts for fast UI display
    APPD_APP_COUNT     NUMBER         DEFAULT 0,
    KIBANA_COUNT       NUMBER         DEFAULT 0,
    SPLUNK_COUNT       NUMBER         DEFAULT 0,
    MONGODB_COUNT      NUMBER         DEFAULT 0,
    PC_COUNT           NUMBER         DEFAULT 0,
    DB_COUNT           NUMBER         DEFAULT 0,
    IS_ACTIVE          CHAR(1)        DEFAULT 'Y' NOT NULL,
    CREATED_BY         VARCHAR2(100),
    CREATED_DATE       DATE           DEFAULT SYSDATE NOT NULL,
    UPDATED_BY         VARCHAR2(100),
    UPDATED_DATE       DATE           DEFAULT SYSDATE,
    CONSTRAINT API_NFT_CHK_TMPL_ACT CHECK (IS_ACTIVE IN ('Y','N')),
    CONSTRAINT API_NFT_UK_TRACK_TMPL UNIQUE (LOB_NAME, TRACK_NAME)
);
COMMENT ON TABLE  API_NFT_TRACK_TEMPLATE IS 'Master template mapping all tool configs to a LOB/Track';
COMMENT ON COLUMN API_NFT_TRACK_TEMPLATE.APPD_APP_IDS IS 'Comma-separated APP_MASTER_IDs from APPD_APPLICATIONS_MASTER';
COMMENT ON COLUMN API_NFT_TRACK_TEMPLATE.KIBANA_CONFIG_IDS IS 'Comma-separated KIBANA_CONFIG_IDs from API_NFT_KIBANA_CONFIG';
CREATE INDEX API_NFT_IDX_TMPL_LOB ON API_NFT_TRACK_TEMPLATE(LOB_NAME, TRACK_NAME, IS_ACTIVE);

-- ============================================================
-- 9. TEST REGISTRATION
--    Records each NFT test registered by PE.
--    Must be created before monitoring starts.
--    Links to API_RUN_MASTER via RUN_ID.
-- ============================================================
CREATE TABLE API_NFT_TEST_REGISTRATION (
    REG_ID               NUMBER         DEFAULT API_NFT_TEST_REG_SEQ.NEXTVAL PRIMARY KEY,
    RUN_ID               VARCHAR2(100)  NOT NULL,  -- FK API_RUN_MASTER.RUN_ID
    PC_RUN_ID            VARCHAR2(50)   NOT NULL,
    LOB_NAME             VARCHAR2(100)  NOT NULL,
    TRACK_NAME           VARCHAR2(100)  NOT NULL,
    TEST_NAME            VARCHAR2(200)  NOT NULL,
    TEST_TYPE            VARCHAR2(50),             -- LOAD|STRESS|ENDURANCE|SPIKE|SOAK
    ENVIRONMENT          VARCHAR2(50)   DEFAULT 'PERF',
    RELEASE_NAME         VARCHAR2(200),
    PEAK_VUSERS          NUMBER,
    RAMP_UP_MINUTES      NUMBER,
    PLANNED_DURATION_HRS NUMBER,
    JENKINS_BUILD        VARCHAR2(100),
    TEMPLATE_ID          NUMBER,                   -- FK API_NFT_TRACK_TEMPLATE.TEMPLATE_ID
    REGISTERED_BY        VARCHAR2(100)  NOT NULL,
    REGISTRATION_DATE    DATE           DEFAULT SYSDATE NOT NULL,
    NOTES                VARCHAR2(2000),
    CONSTRAINT API_NFT_CHK_REG_TYPE CHECK (
        TEST_TYPE IN ('LOAD','STRESS','ENDURANCE','SPIKE','SOAK') OR TEST_TYPE IS NULL),
    CONSTRAINT API_NFT_UK_REG_RUN UNIQUE (RUN_ID),
    CONSTRAINT API_NFT_UK_REG_PC  UNIQUE (PC_RUN_ID)
);
COMMENT ON TABLE  API_NFT_TEST_REGISTRATION IS 'Test registration - must exist before monitoring can start';
COMMENT ON COLUMN API_NFT_TEST_REGISTRATION.RUN_ID IS 'Generated by RunIDGenerator - FK to API_RUN_MASTER.RUN_ID';
CREATE INDEX API_NFT_IDX_REG_LOB     ON API_NFT_TEST_REGISTRATION(LOB_NAME, TRACK_NAME);
CREATE INDEX API_NFT_IDX_REG_DATE    ON API_NFT_TEST_REGISTRATION(REGISTRATION_DATE DESC);
CREATE INDEX API_NFT_IDX_REG_RELEASE ON API_NFT_TEST_REGISTRATION(RELEASE_NAME, LOB_NAME);

-- ============================================================
-- 10. RELEASE REPORTS
--     Full HTML stored as CLOB. Retained 12+ months.
--     Only LOAD / STRESS / ENDURANCE final results.
-- ============================================================
CREATE TABLE API_NFT_RELEASE_REPORTS (
    REPORT_ID           NUMBER         DEFAULT API_NFT_REPORT_SEQ.NEXTVAL PRIMARY KEY,
    RUN_ID              VARCHAR2(100)  NOT NULL,   -- FK API_RUN_MASTER.RUN_ID
    PC_RUN_ID           VARCHAR2(50)   NOT NULL,
    LOB_NAME            VARCHAR2(100)  NOT NULL,
    RELEASE_NAME        VARCHAR2(200)  NOT NULL,
    TEST_TYPE           VARCHAR2(50)   NOT NULL,   -- LOAD|STRESS|ENDURANCE
    TEST_NAME           VARCHAR2(200),
    TRACK_NAME          VARCHAR2(100),
    REPORT_HTML         CLOB           NOT NULL,   -- Complete page HTML
    REPORT_SIZE_KB      NUMBER,
    OVERALL_STATUS      VARCHAR2(20),              -- PASS|FAIL|PARTIAL
    PASS_RATE_PCT       NUMBER,
    PEAK_VUSERS         NUMBER,
    AVG_RESPONSE_MS     NUMBER,
    P95_RESPONSE_MS     NUMBER,
    TOTAL_TRANSACTIONS  NUMBER,
    FAILED_TRANSACTIONS NUMBER,
    RETAIN_UNTIL        DATE,                      -- NULL = keep indefinitely
    IS_ACTIVE           CHAR(1)        DEFAULT 'Y' NOT NULL,
    SAVED_BY            VARCHAR2(100)  NOT NULL,
    SAVED_DATE          DATE           DEFAULT SYSDATE NOT NULL,
    NOTES               VARCHAR2(2000),
    CONSTRAINT API_NFT_CHK_RPT_ACT    CHECK (IS_ACTIVE IN ('Y','N')),
    CONSTRAINT API_NFT_CHK_RPT_TYPE   CHECK (TEST_TYPE IN ('LOAD','STRESS','ENDURANCE')),
    CONSTRAINT API_NFT_CHK_RPT_STATUS CHECK (OVERALL_STATUS IN ('PASS','FAIL','PARTIAL') OR OVERALL_STATUS IS NULL),
    CONSTRAINT API_NFT_UK_RPT_RUN     UNIQUE (RUN_ID, TEST_TYPE)
);
COMMENT ON TABLE  API_NFT_RELEASE_REPORTS IS 'Final test report HTML as CLOB. 12+ month retention.';
COMMENT ON COLUMN API_NFT_RELEASE_REPORTS.REPORT_HTML IS 'Full HTML of test-report.html for long-term viewing via UI';
COMMENT ON COLUMN API_NFT_RELEASE_REPORTS.RETAIN_UNTIL IS 'NULL = retain indefinitely. Regular test data tables cleaned periodically.';
CREATE INDEX API_NFT_IDX_RPT_LOB  ON API_NFT_RELEASE_REPORTS(LOB_NAME, RELEASE_NAME);
CREATE INDEX API_NFT_IDX_RPT_DATE ON API_NFT_RELEASE_REPORTS(SAVED_DATE DESC);
CREATE INDEX API_NFT_IDX_RPT_TYPE ON API_NFT_RELEASE_REPORTS(TEST_TYPE, IS_ACTIVE);

