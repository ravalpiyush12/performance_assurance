-- ============================================================
-- CQE NFT MONITORING PLATFORM - New Table DDL
-- Database : CQE_NFT (HANAINS1U)
-- Prefix   : API_NFT_  (all new tables)
-- Conventions follow existing API_AUTH_* tables:
--   CHAR(1) Y/N flags, SYSDATE defaults,
--   named constraints API_NFT_CHK_* / API_NFT_UK_*,
--   sequences for PKs, CLOB for large/HTML content
-- ============================================================

-- Safe drop (re-installable)
BEGIN
  FOR t IN (SELECT table_name FROM user_tables WHERE table_name IN (
    'API_NFT_USER_LOB_ACCESS','API_NFT_APPD_CONFIG','API_NFT_KIBANA_CONFIG',
    'API_NFT_SPLUNK_CONFIG','API_NFT_MONGODB_CONFIG','API_NFT_PC_CONFIG',
    'API_NFT_DB_CONFIG','API_NFT_TRACK_TEMPLATE',
    'API_NFT_TEST_REGISTRATION','API_NFT_RELEASE_REPORTS'))
  LOOP EXECUTE IMMEDIATE 'DROP TABLE '||t.table_name||' CASCADE CONSTRAINTS'; END LOOP;
END;
/
BEGIN
  FOR s IN (SELECT sequence_name FROM user_sequences WHERE sequence_name IN (
    'API_NFT_KIBANA_SEQ','API_NFT_SPLUNK_SEQ','API_NFT_MONGODB_SEQ',
    'API_NFT_PC_CONFIG_SEQ','API_NFT_DB_CONFIG_SEQ','API_NFT_TRACK_SEQ',
    'API_NFT_REPORT_SEQ','API_NFT_TEST_REG_SEQ','API_NFT_APPD_CFG_SEQ'))
  LOOP EXECUTE IMMEDIATE 'DROP SEQUENCE '||s.sequence_name; END LOOP;
END;
/

