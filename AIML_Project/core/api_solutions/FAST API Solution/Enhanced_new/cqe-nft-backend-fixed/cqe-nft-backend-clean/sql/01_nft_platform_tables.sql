-- ============================================================
-- 1. USER LOB ACCESS
--    Maps API_AUTH_USERS to the LOBs they can access.
--    Admin manages; PE sees only assigned LOBs on login.
-- ============================================================
CREATE TABLE API_NFT_USER_LOB_ACCESS (
    ID              NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    USERNAME        VARCHAR2(100)  NOT NULL,   -- FK API_AUTH_USERS.USERNAME
    LOB_NAME        VARCHAR2(100)  NOT NULL,   -- FK API_LOB_MASTER.LOB_NAME
    GRANTED_BY      VARCHAR2(100)  NOT NULL,
    IS_ACTIVE       CHAR(1)        DEFAULT 'Y' NOT NULL,
    CREATED_DATE    DATE           DEFAULT SYSDATE NOT NULL,
    UPDATED_DATE    DATE           DEFAULT SYSDATE,
    CONSTRAINT API_NFT_CHK_ULA_ACTIVE CHECK (IS_ACTIVE IN ('Y','N')),
    CONSTRAINT API_NFT_UK_USER_LOB    UNIQUE (USERNAME, LOB_NAME)
);
COMMENT ON TABLE  API_NFT_USER_LOB_ACCESS IS 'Maps platform users to accessible LOBs';
COMMENT ON COLUMN API_NFT_USER_LOB_ACCESS.USERNAME IS 'References API_AUTH_USERS.USERNAME';
CREATE INDEX API_NFT_IDX_ULA_USER ON API_NFT_USER_LOB_ACCESS(USERNAME);
CREATE INDEX API_NFT_IDX_ULA_LOB  ON API_NFT_USER_LOB_ACCESS(LOB_NAME, IS_ACTIVE);

