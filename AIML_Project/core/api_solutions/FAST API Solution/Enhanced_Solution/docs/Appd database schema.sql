-- =====================================================
-- 2. Application Discovery Table
-- Stores discovered applications with active/inactive status
-- =====================================================
CREATE TABLE APPD_APPLICATIONS (
    APP_ID NUMBER PRIMARY KEY,
    LOB_ID NUMBER NOT NULL,
    APPLICATION_NAME VARCHAR2(200) NOT NULL,
    APPLICATION_ID VARCHAR2(100),  -- AppD internal ID
    TOTAL_TIERS NUMBER DEFAULT 0,
    TOTAL_NODES NUMBER DEFAULT 0,
    ACTIVE_NODES NUMBER DEFAULT 0,
    INACTIVE_NODES NUMBER DEFAULT 0,
    DISCOVERY_DATE DATE DEFAULT SYSDATE,
    LAST_SEEN_DATE DATE,
    IS_ACTIVE VARCHAR2(1) DEFAULT 'Y',
    CREATED_DATE DATE DEFAULT SYSDATE,
    UPDATED_DATE DATE DEFAULT SYSDATE,
    CONSTRAINT FK_APPD_APP_LOB FOREIGN KEY (LOB_ID) REFERENCES APPD_LOB_CONFIG(LOB_ID),
    CONSTRAINT CHK_APPD_APP_ACTIVE CHECK (IS_ACTIVE IN ('Y', 'N'))
);

CREATE SEQUENCE APPD_APPLICATIONS_SEQ START WITH 1;
CREATE INDEX IDX_APPD_APP_LOB ON APPD_APPLICATIONS(LOB_ID);
CREATE INDEX IDX_APPD_APP_NAME ON APPD_APPLICATIONS(APPLICATION_NAME);
CREATE UNIQUE INDEX UDX_APPD_APP_LOB_NAME ON APPD_APPLICATIONS(LOB_ID, APPLICATION_NAME);


-- =====================================================
-- 3. Tier Discovery Table
-- Stores all tiers within applications
-- =====================================================
CREATE TABLE APPD_TIERS (
    TIER_ID NUMBER PRIMARY KEY,
    APP_ID NUMBER NOT NULL,
    TIER_NAME VARCHAR2(200) NOT NULL,
    TIER_ID_APPD VARCHAR2(100),  -- AppD internal tier ID
    TIER_TYPE VARCHAR2(100),  -- Java, .NET, PHP, etc.
    TOTAL_NODES NUMBER DEFAULT 0,
    ACTIVE_NODES NUMBER DEFAULT 0,
    IS_ACTIVE VARCHAR2(1) DEFAULT 'Y',
    LAST_SEEN_DATE DATE,
    CREATED_DATE DATE DEFAULT SYSDATE,
    UPDATED_DATE DATE DEFAULT SYSDATE,
    CONSTRAINT FK_APPD_TIER_APP FOREIGN KEY (APP_ID) REFERENCES APPD_APPLICATIONS(APP_ID),
    CONSTRAINT CHK_APPD_TIER_ACTIVE CHECK (IS_ACTIVE IN ('Y', 'N'))
);

CREATE SEQUENCE APPD_TIERS_SEQ START WITH 1;
CREATE INDEX IDX_APPD_TIER_APP ON APPD_TIERS(APP_ID);
CREATE INDEX IDX_APPD_TIER_NAME ON APPD_TIERS(TIER_NAME);
CREATE UNIQUE INDEX UDX_APPD_TIER_APP_NAME ON APPD_TIERS(APP_ID, TIER_NAME);


-- =====================================================
-- 4. Node Discovery Table
-- Stores all nodes with active/inactive status based on CPM
-- =====================================================
CREATE TABLE APPD_NODES (
    NODE_ID NUMBER PRIMARY KEY,
    TIER_ID NUMBER NOT NULL,
    APP_ID NUMBER NOT NULL,
    NODE_NAME VARCHAR2(200) NOT NULL,
    NODE_ID_APPD VARCHAR2(100),  -- AppD internal node ID
    MACHINE_NAME VARCHAR2(200),
    IP_ADDRESS VARCHAR2(50),
    
    -- Activity metrics for classification
    CALLS_PER_MINUTE NUMBER DEFAULT 0,
    LAST_ACTIVITY_TIME TIMESTAMP,
    
    -- Classification
    IS_ACTIVE VARCHAR2(1) DEFAULT 'Y',  -- Based on CPM > threshold
    ACTIVE_THRESHOLD NUMBER DEFAULT 10,  -- Minimum CPM to be considered active
    
    -- Metadata
    NODE_TYPE VARCHAR2(100),  -- Java, .NET, etc.
    AGENT_VERSION VARCHAR2(50),
    
    LAST_SEEN_DATE DATE,
    CREATED_DATE DATE DEFAULT SYSDATE,
    UPDATED_DATE DATE DEFAULT SYSDATE,
    
    CONSTRAINT FK_APPD_NODE_TIER FOREIGN KEY (TIER_ID) REFERENCES APPD_TIERS(TIER_ID),
    CONSTRAINT FK_APPD_NODE_APP FOREIGN KEY (APP_ID) REFERENCES APPD_APPLICATIONS(APP_ID),
    CONSTRAINT CHK_APPD_NODE_ACTIVE CHECK (IS_ACTIVE IN ('Y', 'N'))
);

CREATE SEQUENCE APPD_NODES_SEQ START WITH 1;
CREATE INDEX IDX_APPD_NODE_TIER ON APPD_NODES(TIER_ID);
CREATE INDEX IDX_APPD_NODE_APP ON APPD_NODES(APP_ID);
CREATE INDEX IDX_APPD_NODE_ACTIVE ON APPD_NODES(IS_ACTIVE);
CREATE UNIQUE INDEX UDX_APPD_NODE_TIER_NAME ON APPD_NODES(TIER_ID, NODE_NAME);


-- =====================================================
-- 5. Monitoring Runs Table
-- Tracks each monitoring session
-- =====================================================
CREATE TABLE APPD_MONITORING_RUNS (
    RUN_ID VARCHAR2(200) PRIMARY KEY,
    LOB_ID NUMBER NOT NULL,
    LOB_NAME VARCHAR2(100),
    TRACK VARCHAR2(100),
    TEST_NAME VARCHAR2(200),
    
    -- Session details
    START_TIME TIMESTAMP NOT NULL,
    END_TIME TIMESTAMP,
    STATUS VARCHAR2(50) DEFAULT 'RUNNING',  -- RUNNING, COMPLETED, STOPPED, FAILED
    
    -- Configuration
    COLLECTION_INTERVAL_SECONDS NUMBER DEFAULT 1800,
    APPLICATIONS CLOB,  -- JSON array of application names
    
    -- Statistics
    TOTAL_COLLECTIONS NUMBER DEFAULT 0,
    SUCCESSFUL_COLLECTIONS NUMBER DEFAULT 0,
    FAILED_COLLECTIONS NUMBER DEFAULT 0,
    LAST_COLLECTION_TIME TIMESTAMP,
    
    -- Error tracking
    ERROR_MESSAGE VARCHAR2(4000),
    
    CREATED_DATE DATE DEFAULT SYSDATE,
    UPDATED_DATE DATE DEFAULT SYSDATE,
    
    CONSTRAINT FK_APPD_RUN_LOB FOREIGN KEY (LOB_ID) REFERENCES APPD_LOB_CONFIG(LOB_ID),
    CONSTRAINT CHK_APPD_RUN_STATUS CHECK (STATUS IN ('RUNNING', 'COMPLETED', 'STOPPED', 'FAILED'))
);

CREATE INDEX IDX_APPD_RUN_LOB ON APPD_MONITORING_RUNS(LOB_ID);
CREATE INDEX IDX_APPD_RUN_STATUS ON APPD_MONITORING_RUNS(STATUS);
CREATE INDEX IDX_APPD_RUN_START ON APPD_MONITORING_RUNS(START_TIME);


-- =====================================================
-- 6. Server Metrics Table
-- Stores CPU, Memory, Network, Disk metrics
-- =====================================================
CREATE TABLE APPD_SERVER_METRICS (
    METRIC_ID NUMBER PRIMARY KEY,
    RUN_ID VARCHAR2(200) NOT NULL,
    NODE_ID NUMBER NOT NULL,
    COLLECTION_TIME TIMESTAMP NOT NULL,
    
    -- CPU Metrics
    CPU_BUSY_PERCENT NUMBER(5,2),
    CPU_IDLE_PERCENT NUMBER(5,2),
    CPU_STOLEN_PERCENT NUMBER(5,2),
    
    -- Memory Metrics
    MEMORY_TOTAL_MB NUMBER,
    MEMORY_USED_MB NUMBER,
    MEMORY_FREE_MB NUMBER,
    MEMORY_USED_PERCENT NUMBER(5,2),
    
    -- Network Metrics
    NETWORK_INCOMING_KB NUMBER,
    NETWORK_OUTGOING_KB NUMBER,
    
    -- Disk Metrics
    DISK_READS_PER_SEC NUMBER,
    DISK_WRITES_PER_SEC NUMBER,
    DISK_USED_PERCENT NUMBER(5,2),
    DISK_QUEUE_LENGTH NUMBER,
    
    -- Additional metrics as JSON
    ADDITIONAL_METRICS CLOB,
    
    CREATED_DATE DATE DEFAULT SYSDATE,
    
    CONSTRAINT FK_APPD_SERVER_RUN FOREIGN KEY (RUN_ID) REFERENCES APPD_MONITORING_RUNS(RUN_ID),
    CONSTRAINT FK_APPD_SERVER_NODE FOREIGN KEY (NODE_ID) REFERENCES APPD_NODES(NODE_ID)
);

CREATE SEQUENCE APPD_SERVER_METRICS_SEQ START WITH 1;
CREATE INDEX IDX_APPD_SERVER_RUN ON APPD_SERVER_METRICS(RUN_ID);
CREATE INDEX IDX_APPD_SERVER_NODE ON APPD_SERVER_METRICS(NODE_ID);
CREATE INDEX IDX_APPD_SERVER_TIME ON APPD_SERVER_METRICS(COLLECTION_TIME);


-- =====================================================
