-- =====================================================
-- Cleanup Procedure (for maintenance)
-- =====================================================
CREATE OR REPLACE PROCEDURE CLEANUP_OLD_METRICS(
    p_days_to_keep IN NUMBER DEFAULT 90
) AS
BEGIN
    -- Delete old server metrics
    DELETE FROM APPD_SERVER_METRICS 
    WHERE COLLECTION_TIME < SYSDATE - p_days_to_keep;
    
    -- Delete old JVM metrics
    DELETE FROM APPD_JVM_METRICS 
    WHERE COLLECTION_TIME < SYSDATE - p_days_to_keep;
    
    -- Delete old application metrics
    DELETE FROM APPD_APPLICATION_METRICS 
    WHERE COLLECTION_TIME < SYSDATE - p_days_to_keep;
    
    COMMIT;
    
    DBMS_OUTPUT.PUT_LINE('Cleanup completed. Deleted metrics older than ' || p_days_to_keep || ' days.');
END;
