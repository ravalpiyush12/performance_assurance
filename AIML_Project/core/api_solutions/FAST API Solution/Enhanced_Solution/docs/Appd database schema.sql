-- =====================================================
-- AppDynamics Monitoring Database Schema
-- Complete schema for LOB-based monitoring
-- =====================================================

-- =====================================================
-- 1. LOB Configuration Table
-- =====================================================
CREATE TABLE APPD_LOB_CONFIG (
    LOB_ID NUMBER PRIMARY KEY,
    LOB_NAME VARCHAR2(100) NOT NULL UNIQUE,
    LOB_DESCRIPTION VARCHAR2(500),
    APPLICATION_NAMES CLOB,  -- JSON array of AppD application names
    LAST_DISCOVERY_RUN DATE,
    DISCOVERY_SCHEDULE VARCHAR2(50) DEFAULT 'DAILY',  -- DAILY, WEEKLY, etc.
    IS_ACTIVE VARCHAR2(1) DEFAULT 'Y',
    CREATED_DATE DATE DEFAULT SYSDATE,
    UPDATED_DATE DATE DEFAULT SYSDATE,
    CONSTRAINT CHK_APPD_LOB_ACTIVE CHECK (IS_ACTIVE IN ('Y', 'N'))
);

CREATE SEQUENCE APPD_LOB_CONFIG_SEQ START WITH 1;
CREATE INDEX IDX_APPD_LOB_NAME ON APPD_LOB_CONFIG(LOB_NAME);

