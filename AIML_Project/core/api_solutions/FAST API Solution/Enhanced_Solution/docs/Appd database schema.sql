-- Helpful Views
-- =====================================================

-- View: Active nodes per LOB
CREATE OR REPLACE VIEW VW_APPD_ACTIVE_NODES_BY_LOB AS
SELECT 
    l.LOB_NAME,
    a.APPLICATION_NAME,
    t.TIER_NAME,
    n.NODE_NAME,
    n.CALLS_PER_MINUTE,
    n.IS_ACTIVE,
    n.LAST_ACTIVITY_TIME
FROM APPD_LOB_CONFIG l
JOIN APPD_APPLICATIONS a ON l.LOB_ID = a.LOB_ID
JOIN APPD_TIERS t ON a.APP_ID = t.APP_ID
JOIN APPD_NODES n ON t.TIER_ID = n.TIER_ID
WHERE n.IS_ACTIVE = 'Y'
ORDER BY l.LOB_NAME, a.APPLICATION_NAME, t.TIER_NAME, n.NODE_NAME;


-- View: LOB Summary Statistics
CREATE OR REPLACE VIEW VW_APPD_LOB_SUMMARY AS
SELECT 
    l.LOB_ID,
    l.LOB_NAME,
    COUNT(DISTINCT a.APP_ID) as TOTAL_APPLICATIONS,
    COUNT(DISTINCT t.TIER_ID) as TOTAL_TIERS,
    COUNT(n.NODE_ID) as TOTAL_NODES,
    SUM(CASE WHEN n.IS_ACTIVE = 'Y' THEN 1 ELSE 0 END) as ACTIVE_NODES,
    SUM(CASE WHEN n.IS_ACTIVE = 'N' THEN 1 ELSE 0 END) as INACTIVE_NODES,
    l.LAST_DISCOVERY_RUN
FROM APPD_LOB_CONFIG l
LEFT JOIN APPD_APPLICATIONS a ON l.LOB_ID = a.LOB_ID
LEFT JOIN APPD_TIERS t ON a.APP_ID = t.APP_ID
LEFT JOIN APPD_NODES n ON t.TIER_ID = n.TIER_ID
GROUP BY l.LOB_ID, l.LOB_NAME, l.LAST_DISCOVERY_RUN;


