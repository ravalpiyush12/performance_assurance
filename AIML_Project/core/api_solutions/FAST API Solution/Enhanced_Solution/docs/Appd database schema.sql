    GC_MAJOR_COLLECTION_COUNT NUMBER,
    GC_MINOR_COLLECTION_COUNT NUMBER,
    GC_TIME_SPENT_PERCENT NUMBER(5,2),
    
    -- Thread Metrics
    THREAD_COUNT NUMBER,
    THREAD_CURRENT NUMBER,
    THREAD_PEAK NUMBER,
    THREAD_DAEMON NUMBER,
    THREAD_BLOCKED NUMBER,
    
    -- Exception/Error Metrics
